/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint32_t adc_values[ADC_INPUT_NUM] = {'0'};
	uint8_t i = 0, j= 0 ;
	uint8_t canId[ADC_INPUT_NUM]={0};
	char can_msg[8]={'\0'};

	/* Variables for strain conversion */
	float a = 4.9e-7;
	float gain = 500, gf = 2.022, vref = 2.5;
	float e[ADC_INPUT_NUM] = {'0'};
	float voltage_read [ADC_INPUT_NUM] ={'0'};

	/*
	* Each number is exactly 8 bytes. So, one number is sent at a time.
	*/
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

//  HAL_ADC_Init(&hadc1); // Initializes HAL lib
//  HAL_ADCEx_Calibration_Start(&hadc1);

  for (i=0;i<ADC_INPUT_NUM;i++) {
    canId[i] = CAN_DEFAULT_ID + i;
  } // Set the correct IDs for CAN Bus

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_FATFS_Init();
  /* USER CODE BEGIN 2 */

  if (HAL_ADC_Start_DMA(&hadc1, adc_values, ADC_INPUT_NUM) != HAL_OK) {
  	  Error_Handler();
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  for (i=0;i<ADC_INPUT_NUM;i++) {
		  // d8 = Max6675_Read_Temp_1();
		  // d8 = Max6675_Read_Temp_2()
		  voltage_read[i] = ((float)adc_values[i]/(float)ADC_MAX_VALUE) * 3.3;
		  /* Early code for strain gauge measurement */
		  e[i] = 4*(1.52*voltage_read[i] - vref)/(5*gain-2*(1.52*voltage_read[i] - vref)*gf*(1+a*1));
	  }
	  TxHeader.IDE = CAN_ID_EXT;
	  TxHeader.RTR = CAN_RTR_DATA;
	  TxHeader.DLC = 6;

	  i=0;
	  j=0;
	  while (1) {
		 if (i > ADC_INPUT_NUM) {
			 break;
		 }
		 TxHeader.ExtId= canId[j++];
		 memcpy(&can_msg[0], &e[i], 4);
		 memcpy(&can_msg[4], &e[++i], 4); // One float number is 4 bytes, so 2 numbers are sent on one CAN BUS message.
		 while (HAL_CAN_Init(&hcan) != HAL_OK) {}; // wait while CAN initializes
		 while (HAL_CAN_Start(&hcan) != HAL_OK) {}; // wait while CAN starts
		 if (HAL_CAN_GetTxMailboxesFreeLevel(&hcan) > 0) {
			 if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, (uint8_t *)can_msg, &TxMailBox) != HAL_OK) {
				 Error_Handler();
			 } // Send calculated strain values through CAN Network
		 }
		 i++;
	  }
  }
  /* USER CODE END 3 */
}

